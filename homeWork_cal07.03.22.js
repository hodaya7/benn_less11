function add(num1, num2) {
    return num1+num2;
}

function reduce(num1, num2) {
    return num1+-num2;
}

function mul(num1, num2) {
    return num1*num2;
}

function div(num1, num2) {
    return num1/num2;
}

console.log(add(8, 4));
console.log(reduce(8, 4));
console.log(mul(8, 4));
console.log(div(8, 4));
